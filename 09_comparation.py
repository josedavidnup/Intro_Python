# ( > / < ) Mayor / Menor
# ( >= / <= ) Mayor o igual / Menor o igual
# ( == ) Igual
# ( != ) Diferente

print( 2 > 1 ) #--> True
print( 2 > 3 ) #--> False
print(5 > 5 ) #--> False
print(1 < 2) #--> True
print( 2 >= 1 ) #--> True
print( 2 >= 3 ) #--> False
print(1 <= 2) #--> True
print(5 >= 5 ) #-> True
print(4 == 4) #--> True
print('Apple' == 'Apple') #--> True
print('Apple' == 'apple') #--> False
print(4 == 4) # --> False
print('Apple' != 'Apple') #--> False
print('Apple' != 'apple') #--> True

